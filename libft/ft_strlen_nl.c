int	ft_strlen_nl(char *str)
{
	int	i;

	i = 0;
	if (*str == 0 || str == 0)
		return (0);
	while (str[i] != 0 && str[i] != '\n')
		i++;
	if (str[i] == '\n')
		i++;
	return (i);
}
